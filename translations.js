function setLanguage(lang) {
    if (!translations[lang]) return;

    // 1. Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            // Using innerHTML to support <br>, <span> etc.
            el.innerHTML = translations[lang][key];
        }
    });

    // 2. Update all placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });

    // 3. Update active state of language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });

    // 4. Update Document Title
    const path = window.location.pathname;
    const page = path.split("/").pop() || 'index.html';
    let titleKey = 'page_title_home';

    if (page.includes('about')) titleKey = 'page_title_about';
    else if (page.includes('products')) titleKey = 'page_title_products';
    else if (page.includes('production')) titleKey = 'page_title_production';
    else if (page.includes('contacts')) titleKey = 'page_title_contacts';
    else if (page.includes('vacancies')) titleKey = 'page_title_vacancies';
    else if (page.includes('privacy')) titleKey = 'page_title_privacy';
    else if (page.includes('terms')) titleKey = 'page_title_terms';

    if (translations[lang][titleKey]) {
        document.title = translations[lang][titleKey];
    }

    // 5. Save preference and update document
    localStorage.setItem('hygiene_lang', lang);
    document.documentElement.lang = lang;

    // 6. Notify dynamic components (like the product catalog)
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}

// Initial setup on load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('hygiene_lang') || 'ru';

    // 1. Initial translation with a safe delay for components
    setTimeout(() => {
        setLanguage(savedLang);
    }, 150);

    // 2. Setup click listeners for language buttons
    // We use event delegation to catch buttons in dynamic headers
    document.addEventListener('click', (e) => {
        const btn = e.target.closest('.lang-btn');
        if (btn) {
            e.preventDefault();
            const lang = btn.getAttribute('data-lang');
            setLanguage(lang);
        }
    });

    // 3. MutationObserver to handle dynamically added elements (like Web Components)
    const observer = new MutationObserver((mutations) => {
        const currentLang = localStorage.getItem('hygiene_lang') || 'ru';
        let shouldTranslate = false;

        mutations.forEach(mutation => {
            mutation.addedNodes.forEach(node => {
                if (node.nodeType === 1 && (node.hasAttribute('data-i18n') || node.querySelector('[data-i18n]'))) {
                    shouldTranslate = true;
                }
            });
        });

        if (shouldTranslate) {
            setLanguage(currentLang);
        }
    });

    observer.observe(document.body, { childList: true, subtree: true });
});
